volatile uint16_t delay = 0;

void duty_Generator(uint16_t v_duty_u16)
{
	uint16_t v_ton_u16 = ((v_duty_u16 * (M_TOTALTIME)) / 100);
	uint16_t v_toff_u16 = (M_TOTALTIME - v_ton_u16);

	for(delay=0 ; delay <= v_ton_u16 ; delay++)
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_SET);

	for(delay=0 ; delay <= v_toff_u16 ; delay++)
		HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_RESET);
}

int main(void)
{
	uint16_t v_updateDuty_u16 = 10;
	uint8_t b_toggleFlag = 0, b_isPressed=0;
  /* USER CODE BEGIN 1 */


  /* USER CODE END 1 */
  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();

  /* USER CODE BEGIN 2 */

//  1. Generate a square wave of 10khz of 10% duty cycle.
//      1. Read the switch status and increment the duty cycle
//	  by 10% on every press till it reaches 90%.
//	  Upon reaching 90% any new key press should decrease the
//	  duty cycle by 10% till it reaches 10%.


  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */
	  if(GPIO_PIN_SET == HAL_GPIO_ReadPin(GPIOA,GPIO_PIN_0))
	  {
		  b_isPressed = M_PRESSED;
	  }
	  else if(b_isPressed == 1)
	  {
		  b_isPressed = M_RELEASED;

		  if(b_toggleFlag == 0)
		  {
			  v_updateDuty_u16 += 10;
			  if(v_updateDuty_u16 >= 90)
			  {
				  b_toggleFlag = ~b_toggleFlag;
			  }
		  }
		  else
		  {
			  v_updateDuty_u16 -= 10;
			  if(v_updateDuty_u16 <= 10)
			  {
			  	  b_toggleFlag = ~b_toggleFlag;
			  }
		  }


	  }

	 duty_Generator (v_updateDuty_u16);
  /* USER CODE BEGIN 3 */

  }
  /* USER CODE END 3 */

}

